	 UNION ALL

	SELECT CAST(AdAgency_ADDB.dbo.FC_REMOVE_CARACTERES_INVISIVEIS(PROMOTOR_EMAIL) AS NVARCHAR(500)) AS [Email Address]
		  ,CAST(COUNTRY AS NVARCHAR(500)) AS [Country]
