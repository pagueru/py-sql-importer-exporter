SELECT CAST(ISNULL(NOME_DONO_FIRST_NAME,'') AS NVARCHAR(500)) AS [First Name]
	  ,CAST(ISNULL(NOME_DONO_LAST_NAME,'') AS NVARCHAR(500)) AS [Last Name]
	  ,CAST(DONO_EMAIL AS NVARCHAR(500)) AS [Email Address]
	  ,CAST('DONO' AS NVARCHAR(500)) AS [BR_BY_Eleva_Segment]
  FROM (
			SELECT AdAgency_ADDB.dbo.FC_LIMPAR_STRING(NOME_DONO_FIRST_NAME) AS NOME_DONO_FIRST_NAME
				  ,AdAgency_ADDB.dbo.FC_LIMPAR_STRING(NOME_DONO_LAST_NAME) AS NOME_DONO_LAST_NAME
				  ,AdAgency_ADDB.dbo.FC_LIMPAR_STRING(DONO_EMAIL) AS DONO_EMAIL
				  ,AREA_MILHO_SAFRINHA
				  ,LEFT(DONO_EMAIL,1) AS LEFT_DONO_EMAIL
			  FROM Bayer_000_CRM..TB_BASE_ELEVA (NOLOCK)
			 WHERE DONO_EMAIL IS NOT NULL
	   ) AS X
  WHERE DONO_EMAIL IS NOT NULL
	AND AREA_MILHO_SAFRINHA > 0
