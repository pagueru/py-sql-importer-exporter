;WITH CTE_DEDUP AS (
    SELECT *, ROW_NUMBER() OVER (PARTITION BY EMAIL ORDER BY DT_CADASTRO DESC) AS ROW_NUM
      FROM Ambev_014_PerfectDraft.dbo.TB_VTEX_CLIENTE (NOLOCK)
)
SELECT CAST(EMAIL AS NVARCHAR(255)) AS email
      ,CAST(ISNULL(PRIMEIRO_NOME,'Olá') AS NVARCHAR(255)) AS nome
	  ,CAST(AdAgency_ADDB.dbo.FC_HASH_MD5(EMAIL) AS NVARCHAR(255)) AS hash_cliente
	  ,CAST(CIDADE AS NVARCHAR(255)) AS cidade
  FROM CTE_DEDUP
 WHERE ROW_NUM = 1
   AND CIDADE IN ('Campinas','Ribeirão Preto','São José dos Campos')
   AND EMAIL NOT LIKE '%ct.vtex%'
   AND EMAIL IS NOT NULL

