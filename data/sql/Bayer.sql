
--BASE_EMKT_ELEVA_NEWS_POOLS_RTV_20250617
SELECT DISTINCT
    CAST(ISNULL(PRIMEIRO_NOME,'') AS NVARCHAR(500)) AS [First Name]
   ,CAST(ISNULL(AdAgency_ADDB.dbo.UltimoNome(NOME_COMPLETO),'') AS NVARCHAR(500)) AS [Last Name]
   ,CAST(EMAIL AS NVARCHAR(500)) AS [Email Address]
   ,CAST('Brazil' AS NVARCHAR(500)) AS [Country]
  FROM Bayer_000_CRM.dbo.TB_BASE_POOLS (NOLOCK)
 WHERE TIPO_PESSOA IN ('RTV','BAYER')
   AND EMAIL IS NOT NULL

--BASE_EMKT_ELEVA_NEWS_ELEVA_RTV_20250617
SELECT DISTINCT
    CAST(ISNULL(PRIMEIRO_NOME_RTV,'') AS NVARCHAR(500)) AS [First Name]
   ,CAST(ISNULL(AdAgency_ADDB.dbo.UltimoNome(NOME_RTV),'') AS NVARCHAR(500)) AS [Last Name]
   ,CAST(RTV_EMAIL AS NVARCHAR(500)) AS [Email Address]
   ,CAST(ISNULL(COUNTRY,'Brazil') AS NVARCHAR(500)) AS [Country]
  FROM Bayer_000_CRM.dbo.TB_CDP_CLIENTE (NOLOCK)
 WHERE ORIGEM = 'Base Eleva'
   AND RTV_EMAIL IS NOT NULL
