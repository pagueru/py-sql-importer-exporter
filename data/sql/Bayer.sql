SELECT DISTINCT
    CAST(ISNULL(AdAgency_ADDB.dbo.PrimeiroNome(NOME),'') AS NVARCHAR(500)) AS [First Name], CLIENTE
   ,CAST(ISNULL(AdAgency_ADDB.dbo.UltimoNome(NOME),'') AS NVARCHAR(500)) AS [Last Name]
   ,CAST(EMAIL AS NVARCHAR(500)) AS [Email Address]
   ,CAST('DONO' AS NVARCHAR(500)) AS [BR_BY_Eleva_Segment]
   ,CAST(ISNULL(COUNTRY,'Brazil') AS NVARCHAR(500)) AS [Country]
  FROM Bayer_000_CRM.dbo.TB_CDP_CLIENTE (NOLOCK)
 WHERE ORIGEM = 'Base Eleva'
   AND EMAIL IS NOT NULL
