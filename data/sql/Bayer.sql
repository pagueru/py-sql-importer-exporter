--BASE_EMKT_ELEVA_BENEFICIO_GRUPOS_DE_COMPRA_COOPERFIBRA_20250306
SELECT CAST(ISNULL(PRIMEIRO_NOME,'') AS NVARCHAR(500)) AS [First Name]
	  ,LTRIM(RTRIM(CAST(EMAIL AS NVARCHAR(500)))) AS [Email Address]
	  ,CAST('Brazil' AS NVARCHAR(500)) AS [Country]
  FROM Bayer_002_DBM.[dbo].[TMP_BAYER_BASE_POOLS_FINAL_v3_20250306]
 WHERE BASE = 'Participantes Cooperfibra'
   AND NULLIF(EMAIL,'') IS NOT NULL
 ORDER BY TIPO_PESSOA DESC

